import argparse
import csv
import json
import math
from pathlib import Path

import numpy as np


def parse_args():
    parser = argparse.ArgumentParser(
        description="Summarize FrozenLake offline comparison jsonl (OCBA vs Uniform)."
    )
    parser.add_argument(
        "--input",
        type=Path,
        default=Path("logs/openevolve_frozenlake/offline_compare.jsonl"),
        help="Path to offline compare jsonl generated by run_offline_compare.py",
    )
    parser.add_argument(
        "--csv-output",
        type=Path,
        default=None,
        help="Optional path to export per-candidate summary CSV.",
    )
    parser.add_argument(
        "--ci-z",
        type=float,
        default=1.96,
        help="Z value for confidence interval (default: 1.96).",
    )
    return parser.parse_args()


def _safe_float(value, default=0.0):
    try:
        x = float(value)
        if math.isfinite(x):
            return x
    except Exception:
        pass
    return float(default)


def _mean_ci(values: list[float], z: float) -> tuple[float, float, float]:
    arr = np.array(values, dtype=np.float64)
    if arr.size == 0:
        return 0.0, 0.0, 0.0
    mean = float(np.mean(arr))
    if arr.size <= 1:
        return mean, mean, mean
    sem = float(np.std(arr, ddof=1) / np.sqrt(arr.size))
    half = float(z * sem)
    return mean, mean - half, mean + half


def _load_records(path: Path) -> list[dict]:
    rows = []
    with path.open("r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            rows.append(json.loads(line))
    return rows


def summarize(records: list[dict], z: float) -> dict:
    delta_budget = []
    delta_budget_ratio = []
    delta_score = []
    delta_best = []
    ocba_budget = []
    uniform_budget = []
    ocba_early_stop = []
    uniform_early_stop = []

    per_candidate_rows = []

    for rec in records:
        ocba = rec.get("ocba", {}) or {}
        uniform = rec.get("uniform", {}) or {}

        ocba_b = _safe_float(ocba.get("budget_used", 0.0), 0.0)
        uniform_b = _safe_float(uniform.get("budget_used", 0.0), 0.0)
        d_b = ocba_b - uniform_b
        save_ratio = 0.0 if uniform_b <= 0 else (uniform_b - ocba_b) / uniform_b

        d_score = _safe_float(rec.get("delta_combined_score_ocba_minus_uniform", 0.0), 0.0)
        d_best = _safe_float(rec.get("delta_best_true_reward_ocba_minus_uniform", 0.0), 0.0)

        ocba_es = _safe_float(ocba.get("early_stop_triggered", 0.0), 0.0)
        uniform_es = _safe_float(uniform.get("early_stop_triggered", 0.0), 0.0)

        delta_budget.append(d_b)
        delta_budget_ratio.append(save_ratio)
        delta_score.append(d_score)
        delta_best.append(d_best)
        ocba_budget.append(ocba_b)
        uniform_budget.append(uniform_b)
        ocba_early_stop.append(ocba_es)
        uniform_early_stop.append(uniform_es)

        per_candidate_rows.append(
            {
                "program_path": str(rec.get("program_path", "")),
                "ocba_budget_used": ocba_b,
                "uniform_budget_used": uniform_b,
                "delta_budget_used_ocba_minus_uniform": d_b,
                "ocba_budget_save_ratio_vs_uniform": save_ratio,
                "ocba_combined_score": _safe_float(ocba.get("combined_score", -1e9), -1e9),
                "uniform_combined_score": _safe_float(uniform.get("combined_score", -1e9), -1e9),
                "delta_combined_score_ocba_minus_uniform": d_score,
                "ocba_best_true_reward": _safe_float(ocba.get("best_true_reward", -1e9), -1e9),
                "uniform_best_true_reward": _safe_float(uniform.get("best_true_reward", -1e9), -1e9),
                "delta_best_true_reward_ocba_minus_uniform": d_best,
                "ocba_early_stop_triggered": int(ocba_es > 0.5),
                "uniform_early_stop_triggered": int(uniform_es > 0.5),
            }
        )

    n = len(records)
    mean_db, low_db, high_db = _mean_ci(delta_budget, z)
    mean_sr, low_sr, high_sr = _mean_ci(delta_budget_ratio, z)
    mean_ds, low_ds, high_ds = _mean_ci(delta_score, z)
    mean_dr, low_dr, high_dr = _mean_ci(delta_best, z)

    return {
        "n_candidates": n,
        "delta_budget_used_ocba_minus_uniform": {
            "mean": mean_db,
            "ci_low": low_db,
            "ci_high": high_db,
        },
        "ocba_budget_save_ratio_vs_uniform": {
            "mean": mean_sr,
            "ci_low": low_sr,
            "ci_high": high_sr,
        },
        "delta_combined_score_ocba_minus_uniform": {
            "mean": mean_ds,
            "ci_low": low_ds,
            "ci_high": high_ds,
        },
        "delta_best_true_reward_ocba_minus_uniform": {
            "mean": mean_dr,
            "ci_low": low_dr,
            "ci_high": high_dr,
        },
        "ocba_budget_used_mean": float(np.mean(np.array(ocba_budget, dtype=np.float64))) if n else 0.0,
        "uniform_budget_used_mean": float(np.mean(np.array(uniform_budget, dtype=np.float64))) if n else 0.0,
        "ocba_early_stop_rate": float(np.mean(np.array(ocba_early_stop, dtype=np.float64))) if n else 0.0,
        "uniform_early_stop_rate": float(np.mean(np.array(uniform_early_stop, dtype=np.float64))) if n else 0.0,
        "per_candidate_rows": per_candidate_rows,
    }


def maybe_write_csv(path: Path | None, rows: list[dict]) -> None:
    if path is None:
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    fieldnames = [
        "program_path",
        "ocba_budget_used",
        "uniform_budget_used",
        "delta_budget_used_ocba_minus_uniform",
        "ocba_budget_save_ratio_vs_uniform",
        "ocba_combined_score",
        "uniform_combined_score",
        "delta_combined_score_ocba_minus_uniform",
        "ocba_best_true_reward",
        "uniform_best_true_reward",
        "delta_best_true_reward_ocba_minus_uniform",
        "ocba_early_stop_triggered",
        "uniform_early_stop_triggered",
    ]
    with path.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def main():
    args = parse_args()
    if not args.input.exists():
        raise FileNotFoundError(f"Input file not found: {args.input}")

    records = _load_records(args.input)
    summary = summarize(records, z=args.ci_z)
    maybe_write_csv(args.csv_output, summary.pop("per_candidate_rows"))

    print(json.dumps(summary, ensure_ascii=True, indent=2))
    if args.csv_output is not None:
        print(f"csv_saved={args.csv_output}")


if __name__ == "__main__":
    main()
